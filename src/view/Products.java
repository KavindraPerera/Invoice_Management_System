    private void editProductLinkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editProductLinkButtonActionPerformed
        int index = productTable.getSelectedRow();
        TableModel model = productTable.getModel();

        Product product = new Product();
        product.setProductId(Integer.parseInt(model.getValueAt(index, 0).toString()));
        product.setProductName(model.getValueAt(index, 1).toString());
        product.setDescripion(model.getValueAt(index, 2).toString());
        product.setQuantity(Integer.parseInt(model.getValueAt(index, 3).toString()));
        product.setPurchasePrice(new BigDecimal(model.getValueAt(index, 4).toString()));
        product.setSellingPrice(new BigDecimal(model.getValueAt(index, 5).toString()));

        EditProduct productEdit = new EditProduct(product);
        productEdit.setVisible(true);
        productEdit.pack();
        productEdit.setLocationRelativeTo(null);


    }//GEN-LAST:event_editProductLinkButtonActionPerformed

