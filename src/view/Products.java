    void displayProducts(List<Product> products) {
        DefaultTableModel model = (DefaultTableModel) productTable.getModel();
        model.setRowCount(0);
        Object[] row = new Object[6];
        for (int i = 0; i < products.size(); i++) {
            row[0] = products.get(i).getProductId();
            row[1] = products.get(i).getProductName();
            row[2] = products.get(i).getDescripion();
            row[3] = products.get(i).getQuantity();
            row[4] = products.get(i).getPurchasePrice();
            row[5] = products.get(i).getSellingPrice();

            model.addRow(row);

        }
        productTable.setModel(model);
    }
